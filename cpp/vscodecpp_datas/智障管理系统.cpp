bool Compare(char a[],char b[]){
	char swap_a[100];
	char swap_b[100];
	strcpy(swap_a,a);
	strcpy(swap_b,b);
//	cout<<swap_a<<" "<<swap_b<<endl;
	int len=max(strlen(swap_a),strlen(swap_b));
	int sum=0;
	for(int i=0;i<len;i++){
		if(swap_a[i]==swap_b[i]){
			sum++;
		}
	}
	if(sum*1.0/len>=0.5){
		return true;
	}
	return false;
}
